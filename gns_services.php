<p>Người ta ngày càng quan tâm đến thuật ngữ này kể từ khi thuật ngữ được giới thiệu hồi tháng 4 năm 2011 bởi Kagermann/Lukas/Wahlster (2011 – Tài Liệu Tham Khảo 1). Nhờ sự thúc đẩy của nhóm công tác gồm chuyên gia trong các cộng đồng khoa học và kinh doanh, tầm nhìn đã được đưa ra cho ngành công nghiệp Đức và dưới dạng các khuyến nghị dành cho chính phủ liên bang. Kết quả là, một chương trình nghiên cứu đã được xây dựng, với nguồn vốn khoảng 200 triệu euro trong vài năm tới. Bên cạnh đó, ba hiệp hội công nghiệp lớn của Đức (VDMA, ZVEI, và BitKom) đã tham gia vào lực lượng nhằm tạo ra một diễn đàn chung để hỗ trợ công tác điều phối tất cả các hoạt động của Công Nghiệp 4.0. <p>
