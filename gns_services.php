<p><strong>Điện toán biết nhận thức (Cognitive Computing)</strong></p>
<p>“Điện toán biết nhận thức là giai đoạn thứ ba và cũng là giai đoạn mang tính đột phá cao nhất trong lịch sử phát triển của công nghệ điện toán” theo Mr. Eric Yeo, Tổng Giám Đốc IBM Việt Nam phát biểu tại hội thảo IBM Business Connect, IBM Việt Nam vào tháng 18/03/2016.</p>
<p>Điện toán nhận thức được coi là đặt dấu chấm hết cho “Thời đại của trí tuệ nhận tạo (AI)”, làm thay đổi hoàn toàn phương thức tương tác giữa con người và hệ thống, trong đó những hệ thống thông minh sẽ mô phỏng những năng lực của bộ não con người nhằm góp phần giải quyết những vấn đề phức tạp nhất của xã hội.</p>
<p>“Điện toán biết nhận thức” là khái niệm dùng để mô tả những hệ thống có khả năng học hỏi ở quy mô nhanh và sâu, luận giải có mục đích, và tương tác với con người thông qua ngôn ngữ tự nhiên.</p>
<p>“Để hiểu rõ tương lai của điện toán biết nhận thức, điều quan trọng là phải đặt nó vào trong bối cảnh lịch sử”, ông Eric Yeo, Tổng Giám Đốc IBM Việt Nam nói. “Từ trước đến nay, công nghệ điện toán được chia là hai kỷ nguyên rõ rệt – Kỷ nguyên của máy tính bìa đục lỗ (Tabulating, với đại diện là chiếc máy tính đơn giản)” và Kỷ nguyên của máy tính lập trình được (Programming – từ máy chủ mainframe cho đến máy tính cá nhân, điện thoại thông minh và máy tính bảng đều sinh ra trong kỷ nguyên này). </p>
<p>Các hệ thống điện toán nhận thức sẽ giúp chúng ta hiểu được 80% lượng dữ liệu “phi cấu trúc” trong số 2.5 ngàn tỷ tỷ tỷ (quintillion) bytes dữ liệu sản sinh mỗi ngày từ mang xã hội, di động, Internet của vạn vật, và từ mọi hoạt động diễn ra trên hành tinh chúng ta. Điều đó cho phép con người theo kịp khối lượng, độ phức tạp và bản chất không dự đoán được của thông tin và các hệ thống trong thế giới hiện đại. Hiện tại chương trình IBM Watson đã được triển khai rộng rãi trên nền tảng đám mây, được hỗ trợ bởi các năng lực công nghệ mạnh mẽ của IBM trong nhiều lĩnh vực, như phân tích dữ liệu, tiêu chuẩn mở, bảo mật cũng như các kiến thức sâu rộng về các ngành nghề thuộc nhiều lĩnh vực khác nhau như: lĩnh vực y tế, lĩnh vực bán lẻ, lĩnh vực ngân hàng, lĩnh vực thể thao, lĩnh vực y tế và sức khỏe, lĩnh vực khách sạn.</p>
<p>Tại GNS, chúng tôi tiếp cận các thông tin, tài liệu khoa học kỹ thuật Điện toán biết nhận thức làm nền tảng nghiên cứu và phát triển (R&D) các ứng dụng dưới hình thức module 4.0 với các phiên bản và chức năng khác nhau hỗ trợ phần mềm quản lý doanh nghiệp. Bên cạnh đó, chúng tôi hợp tác nhiều hình thức khác nhau với đối tác thứ 3 về tài sản trí tuệ, bản quyền để được chia sẻ thông tin, tiếp nhận nhượng quyền chuyển giao công nghệ mã nguồn 4.0 vào sản phẩm của mình. </p>

<h4 class="tbk__subtitle fs-xl fw-thin" style="margin-top: 50px;">
	Nền tảng bảo mật dữ liệu trong thiết kế phần mềm, bảo mật trong cung cấp dịch vụ cần biết ?
</h4>
<p>Phần mềm có ít những lỗ hổng bảo mật đồng nghĩa với việc chất lượng của phần mềm tốt hơn và giá thành giảm đi. Nhằm tránh được lặp lại các lỗi lầm gây ra các phần mềm và hệ thống không bảo mật và không đáng tin cậy, thì phần mềm cần hướng đến các yếu tố sau:</p>
<p><strong>Người dùng mong chờ gì ở phần mềm?</strong></p>
<p>Phần lớn người sử dụng phần mềm mua phần mềm bởi nó có thể đáp ứng được nhu cầu của họ nhằm thực hiện một số chức năng nào đó. Nhưng họ lại ít quan tâm đến phần mềm đó viết code có chất lượng không. Khi bạn mua một gói phần mềm, bạn thường thừa nhận chúng sẽ hoạt động giống như những gì đã được quảng cáo và bạn cũng sẽ không nghĩ về việc chương trình này sẽ thực hiện công việc của nó tốt như thế nào.</p>
<p>Sự thật đáng buồn là hầu hết phần mềm vẫn có những sai sót và những sai sót này có thể đe dọa ảnh hưởng tới độ bảo mật an toàn của bất kì một hệ thống nào chúng đang hoạt động trên. Các nhà lập trình được dạy để viết code – họ không được dạy cách để viết code tốt.</p>
<p>Các tổ chức khuyến khích các nhà lập trình viết nhiều code, hơn là vì code tốt trong bối cảnh code giá rẻ và code được phát triển nhanh chóng đang thống trị thị trường.
<p>Đặc biệt, các ứng dụng Web rất dễ có khả năng bị hỏng với một số loại lỗ hỏng (ví dụ Cross Site Scripting (XSS) trừ phi nhà lập trình nó nỗ lực ngăn chặn chúng. Nếu nhà lập trình viên thất bại trong việc thêm những đoạn mã hóa đầu ra và xác nhận đầu vào phù hợp, ứng dụng này sẽ dễ dàng bị tấn công theo mặc định tới XSS).</p>
<p>Đối với một nhà lập trình, phần mềm này có thể hoạt động như mục tiêu làm việc đặt ra nhưng không bao giờ kiểm tra xem chúng hoạt động thế nào khi chúng có một số mã độc hoặc bị tấn công trực tiếp.</p>
<p>Viết phần mềm, giống như khi lái một chiếc xe, là một thói quen. Trước khi có ai đó dạy chúng ta cách lái xe an toàn., chúng ta hầu như không biết sự nguy hiểm của lái xe và những kỹ năng cần thiết để ngăn chặn hoặc tránh các vụ tai nạn. Ô tô thơpfng có những cơ chế an toàn được xây dựng sẵn, nhưng khi lái xe, chúng ta sẽ phải hoàn toàn sử dụng các kỹ năng lái xe an toàn.</p>
<p><strong>Vấn đề này tệ đến mức nào?</strong></p>
<p>Năm 2008, những lỗ hổng được tìm ra từ các phần mềm thương mại đã gia tăng đáng kể - tăng 13,5% so với năm 2007. Độ nghiêm trọng của các lỗ hỏng cũng tăng lên với 15,3%. Những lỗ hổng có độ nghiêm trọng vừa có mức tăng 67,5% và có gần 92% các lỗ hổng của năm 2008 đều có thể bị khao thác từ xa.</p>
<p>Trong số các lỗ hổng được phát hiện vào năm 2008, chỉ có 47% lỗ hổng có thể vá được bằng các bản vá của nhà cung cấp. Cuối năm 2008, 74% các lỗ hổng ứng dụng web được tiết lộ năm này cũng không có một bản vá nào để vá chúng.</p>
<p><strong>Nhà phát triển phần mềm cần xây dựng cho mình một mô hình bảo mật phù hợp ?</strong></p>
<p>Các lỗ hổng phần mềm xuất hiện trong phần mềm bởi cùng với đặc điểm kỹ thuật, triển khai và vòng kiểm tra, các yêu cầu dành cho bảo mật phần mềm không được quan tâm và bị xao lãng. Phần mềm chỉ được bảo mật khi nó được thiết kế dành cho bảo mật – hầu hết những sự cố gắng trong việc nâng cấp bảo mật sau khi có vấn đề thậm chí còn phức tạp hơn nhiều so với khi chúng được cân nhắc ngay từ khi mở đầu phát triển.</p>
<p>Để có thể có phát triển những yêu cầu bảo mật, bảo mật nên được xây dựng trong chu trình phát triển và bắt đầu với một công cụ quản lý được hỗ trợ bởi Secure Coding Initiative nhằm nâng cấp được cách nghĩ một chức sẽ thế nào và phát triển phần mềm cho tất cả mọi người có thể sử dụng – từ sử dụng gia đình hoặc bán cho ai đó.</p>
<p>Bất kì một code bảo mật ban đầu nào đều phải đáp ứng được tất cả các bước của chu trình phát triển của một phần mềm. Các chương trình bảo mật được an toàn theo thiết kế, trong suốt quá trình phát triển và theo mặc định. Với bất kì một thay đỏi ban đầu nào, giáo dục luôn đóng vai trò quan trọng. Giáo dục là “viên đá đặt nền móng” cho bất kì một sự can thiệp hiệu quả nào nhằm nâng cấp chất lượng phần mềm. Ưu tiên dựa vào những người có kinh nghiệm trong việc phát triển phần mềm có code bảo mật, cũng như rất cần thiết trong việc đào tạo thiết kế an toàn và quản lý code nhằm giúp cho các nhà phân tích, thiết kế và các nhà phát triển có thể hiểu rõ ràng hơn về cách những phân tích chưa hoàn thành, quyết định thiết kế tồi, và các hành động viết code tồi phải chịu trách nhiêm về những lỗ hổng phần mềm và hệ thống.</p>
<p>Một khi đã được phổ biến kiến thức, những người tham dự chu trình phát triển phần mềm (SDLC - Software Development Life - Cycle) sẽ hành động khác đi và bắt đầu hoạt động như cơ chế kiểm tra và cân bằng bên trong giúp nắm bắt được vấn đề sớm hơn cũng như giúp giảm chi phí phát triển và tăng chất lượng hệ thống.</p>
<p>Giáo dục cần chuẩn bị nhân lực có kiến thức cơ bản về lỗ hổng các ứng dụng web, là cho họ quen với các công cụ hack, cũng như chuẩn bị cho họ những công cụ và kỹ năng mới giúp họ thực hiện công việc của mình tốt hơn.</p>
<p>Ở những ngày đầu của phát triển phần mềm, các nghiên cứu đã cho thấy chi phí chỉnh sửa các lỗi, lỗ hổng trong thiết kế thấp hơn rất nhiều khi những vấn đề này bị phát hiện và chữa lỗi với các yêu cầu, giai đoạn thiết kế hơn là sau khi họ bắt đầu sản xuất phần mềm. Vì vậy, nếu nhà phát triển tích hợp các phương pháp bảo mật với chu trình phát triển càng sớm, giá của phần mềm sẽ rẻ.</p>
