<p><strong>Điện toán đám mây hay còn gọi là điện toán máy chủ ảo (cloud computing): </strong></p>
<p>Điện toán đám mây (Cloud computing) được đánh giá là xu hướng phát triển trong ngành công nghệ phần mềm quản lý doanh nghiệp (Cloud ERP). Trong vài năm trở lại đây, điện tóa đám mây đã tạo ra một cuộc cách mạng trong ngành công nghiệp máy tính, thay đổi cơ bản cách thức lưu trữ, chia sẻ và xử lý dữ liệu. Đa số chúng ta đều đã và đang sử dụng một hoặc nhiều dịch vụ ứng dụng công nghệ điện toán đám mây trong đời sống hàng ngày cũng như trong quản lý doanh nghiệp, như G-mail, Google Drive, …</p>
<p>Nói một cách đơn giản, điện toán đám mây là việc ảo hóa các tài nguyên tính toán và các ứng dụng thay vì việc bạn sử dụng một hoặc nhiều máy chủ thật (ngay trước mắt, có thể sờ được, có thể tự bạn ấn nút bật tắt được) thì nay bạn sẽ sử dụng các tài nguyên được ảo hóa thông qua môi trường internet. Bản thân từ đám mây (cloud) là một từ ẩn dụ cho Internet. Điều này có nghĩa là, dữ liệu của bạn sẽ được lưu trữ trên Internet. Giống như việc khi bạn truy cập vào trang web gmail.com, các thư của bạn được lưu trưc trên hệ thống của gmail thay vì lữu trữ ở máy tính của bạn.</p>
<p>Cloud ERP là phần mềm ERP dựa trên điện toán đám mây chứ không dựa vào cơ sở hạ tầng máy chủ, giúp doanh nghiệp chia sẻ thông tin giữa các phòng ban. Phần mềm Cloud ERP tích hợp một vài hoặc tất cả các chức năng cần thiết để một doanh nghiệp hoạt động, như: quản lý khách hàng, quản lý nguồn lực, quản lý đơn hàng, kế toán, hàng tồn kho, quản lý dự án,… thành một hệ thống hoàn chỉnh, và toàn bộ dữ liệu của doanh nghiệp được quản lý bởi Cloud ERP sẽ được lưu trữ ở trên mây.</p>
